# ------------------------------------------------------------
# Включаем UTF-8, чтобы корректно отображались русские символы
# ------------------------------------------------------------
[Console]::OutputEncoding = [System.Text.Encoding]::UTF8
$OutputEncoding = [System.Text.Encoding]::UTF8


# ------------------------------------------------------------
# Настройки скрипта
# ------------------------------------------------------------

# Интервал обновления данных (в секундах)
$interval = 10

# Количество логических ядер процессора
$processorCount = [Environment]::ProcessorCount

# Сколько процессов показывать в TOP
$topCount = 10


# ------------------------------------------------------------
# Настройки форматирования вывода
# ------------------------------------------------------------

# Заголовки таблиц
$headerCPU = "=== TOP $topCount по загрузке CPU ==="
$headerRAM = "=== TOP $topCount по использованию RAM ==="

# Формат строки вывода процесса
$formatString = "{0,-25} PID:{1,-8} Потоки:{2,-6} CPU:{3,8:F2} sec.  RAM:{4,8:F2} MB"


# ------------------------------------------------------------
# Цвета для первых трёх процессов в списке
# ------------------------------------------------------------

# 1 место — Yellow
# 2 место — Cyan
# 3 место — Green
$colors = @([ConsoleColor]::Yellow, [ConsoleColor]::Cyan, [ConsoleColor]::Green)


# ------------------------------------------------------------
# Функция отображения списка процессов
# ------------------------------------------------------------
function Show-TopList {

    param($list, $title)
    
    # Вывод заголовка таблицы
    Write-Host $title
    
    # Перебор всех процессов из списка
    for ($i = 0; $i -lt $list.Count; $i++) {

        $p = $list[$i]

        # Формируем строку вывода
        $line = $formatString -f $p.Name, $p.Id, $p.Threads, $p.CPU, $p.RAM
        
        # Подсветка первых трёх процессов
        if ($i -lt $colors.Count) {
            Write-Host $line -ForegroundColor $colors[$i]
        }
        else {
            Write-Host $line
        }
    }
    
    # Пустая строка между таблицами
    Write-Host ""
}


# ------------------------------------------------------------
# Основной цикл мониторинга
# ------------------------------------------------------------
while ($true) {

    # Очистка экрана перед обновлением
    Clear-Host

    # Установка курсора в начало консоли
    [console]::SetCursorPosition(0, 0)
    

    # --------------------------------------------------------
    # Получаем список процессов
    # --------------------------------------------------------
    # Формируем собственный объект с нужными полями
    $processes = Get-Process | ForEach-Object {

        [PSCustomObject]@{

            # Имя процесса
            Name    = $_.ProcessName

            # PID процесса
            Id      = $_.Id

            # CPU время процесса (в секундах)
            CPU     = [math]::Round($_.CPU, 2)

            # Используемая память (MB)
            RAM     = [math]::Round($_.WorkingSet64 / 1MB, 2)

            # Количество потоков
            Threads = $_.Threads.Count
        }
    }
    

    # --------------------------------------------------------
    # Формируем TOP списки процессов
    # --------------------------------------------------------

    # TOP процессов по CPU
    $topCPU = $processes |
        Sort-Object CPU -Descending |
        Select-Object -First $topCount

    # TOP процессов по использованию RAM
    $topRAM = $processes |
        Sort-Object RAM -Descending |
        Select-Object -First $topCount
    

    # --------------------------------------------------------
    # Вывод таблиц процессов
    # --------------------------------------------------------

    Show-TopList -list $topCPU -title $headerCPU
    Show-TopList -list $topRAM -title $headerRAM
    

    # --------------------------------------------------------
    # Общая статистика системы
    # --------------------------------------------------------

    # Общее количество процессов
    $totalProcesses = $processes.Count

    # Суммарное CPU время всех процессов
    $totalCPU = ($processes | Measure-Object CPU -Sum).Sum

    # Суммарное использование RAM
    $totalRAM = ($processes | Measure-Object RAM -Sum).Sum
    

    # Вывод статистики
    Write-Host "Всего процессов: $totalProcesses | Всего CPU: $totalCPU sec | Всего RAM: $totalRAM MB"

    # Средняя загрузка CPU на одно ядро
    Write-Host "Средняя загрузка CPU на ядро: $([math]::Round($totalCPU / $processorCount, 2)) sec"

    Write-Host ""

    # Пояснение показателей
    Write-Host "CPU = секунды процессорного времени (всего на $processorCount ядрах)"
    Write-Host "Обновление каждые $interval сек. Нажмите Ctrl+C для выхода."
    

    # --------------------------------------------------------
    # Пауза перед следующим обновлением
    # --------------------------------------------------------
    Start-Sleep -Seconds $interval
}